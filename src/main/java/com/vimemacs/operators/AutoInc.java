package com.vimemacs.operators;

/**
 * @author HWD
 * @date 2022/12/21 10:30
 */
public class AutoInc {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("i: " + i);
        System.out.println("++i: " + ++i);
        System.out.println("i++: " + i++);
        System.out.println("i: " + i);
        System.out.println("--i: " + --i);
        System.out.println("i--: " + i--);
        System.out.println("i: " + i);
    }
}
